    case "مفروشات":
      return "Furniturs";
    case "حواسيب":
      return "Computers";
    case "هدايا":
      return "Gifts";
    case "مستلزمات أطفال":
      return "Babies stuff";
    case "دراجات":
      return "Motorcycles";
    case "رياضة":
      return "Sport";
    case "صيدليات":
      return "Pharmacies";
    case "خدمات":
